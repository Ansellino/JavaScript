let age = 25;

// Unary operator adalah operator yang hanya membutuhkan satu operand
typeof age; // Mengembalikan tipe data dari variabel age

// Binary operator adalah operator yang membutuhkan dua operand
5 + 4;
10 / 2;
age = 10;

// Ternary operator adalah operator yang membutuhkan tiga operand
age < 10 ? "You are too young" : "welcome onboard";

// Digunakan untuk menginisiasi nilai

const name = "Dicoding";
let location = "Bandung";

// digunakan untuk mengubah nilai
location = "Jakarta";

// Arithmetic Operator

6 + 5; // mengembalikan 11
7 - 2; // mengembalikan 5
8 * 5; // mengembalikan 40
10 / 3; // mengembalikan 3.33
10 % 2; // mengembalikan 0
2 * (10 + 2); // mengembalikan 24

// AND
console.log(true && true); // true
console.log(false && true); // false
console.log(true && false); // false
console.log(false && false); // false
console.log(5 === 5 && 3 < 5); // true

// OR
console.log(true || true); // true
console.log(false || true); // true
console.log(true || false); // true
console.log(false || false); // false
console.log(5 === 5 || 3 > 5); // true

// NOT
console.log(!true); // false
console.log(!false); // true

// String Operator
const first = "bekerja";
const second = "sama";
const merged = first + second;

console.log(merged); // Output: bekerjasama
